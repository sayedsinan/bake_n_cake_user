class UserDetails {
  final int mobileNumber;
  final String address;
  final String email;
  final String dateOfBirth;
  final String name;
  final String image;
  final String docId;

  UserDetails({
    required this.mobileNumber,
    required this.address,
    required this.email,
    required this.dateOfBirth,
    required this.name,
    required this.image,
    required this.docId,
  });
}
